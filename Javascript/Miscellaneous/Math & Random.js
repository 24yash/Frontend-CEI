// Math Constants
console.log(Math.PI); // PI
console.log(Math.E); // Euler's Number
console.log(Math.LN2); // Natural log of 2
console.log(Math.LN10); // Natural log of 10
console.log(Math.LOG2E); // Base-2 logarithm of E
console.log(Math.LOG10E); // Base-10 logarithm of E
console.log(Math.SQRT2); // Square root of 2
console.log(Math.SQRT1_2); // Square root of 1/2

// Basic Math Methods
console.log(Math.pow(8, 2)); // Power
console.log(Math.sqrt(64)); // Square root
console.log(Math.cbrt(27)); // Cubic root
console.log(Math.abs(-4.7)); // Absolute value
console.log(Math.ceil(1.4)); // Round up
console.log(Math.floor(1.4)); // Round down
console.log(Math.round(1.5)); // Round to nearest integer

// Trigonometric Methods
console.log(Math.sin(Math.PI / 2)); // Sine
console.log(Math.cos(0)); // Cosine
console.log(Math.tan(Math.PI / 4)); // Tangent

// Logarithmic and Exponential Methods
console.log(Math.exp(1)); // e^x
console.log(Math.log(10)); // Natural logarithm

// Max, Min, and Hypotenuse
console.log(Math.max(5, 10, 25)); // Maximum
console.log(Math.min(5, 10, 25)); // Minimum
console.log(Math.hypot(3, 4)); // Hypotenuse

// Math.sign
console.log(Math.sign(-5)); // -1
console.log(Math.sign(0)); // 0
console.log(Math.sign(5)); // 1

// Random Numbers
console.log(Math.random()); // Random number between 0 and 1
console.log(Math.random() * 10); // Random number between 0 and 10
console.log(Math.random() * 100); // Random number between 0 and 100
console.log(Math.floor(Math.random() * 10 + 1)); // Random whole number between 1 and 10
console.log(Math.floor(Math.random() * 100 + 1)); // Random whole number between 1 and 100
